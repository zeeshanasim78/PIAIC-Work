// Loop are use to do repeated tasks. Loops are handy if you wna to run the same code multiple times
// There are three types of loop for , while and do while
// for loop syntax is as under:
// for(expression 1; expression 2 ; expression 3) {
//}
/*
expression 1 is the intitlization of variable from what value it should start
expression 2 is the condition till then it should run
expression 3 is increment
*/
// For Loop With Post Increment
for (var i = 1; i <= 5; i++) {
    console.log(" My name is Zeeshan : Loop Iteration # ".concat(i));
}
// For Loop With Pre Increment
for (var i = 1; i <= 5; ++i) {
    console.log(" My name is Zeeshan : Loop Iteration # ".concat(i));
}
