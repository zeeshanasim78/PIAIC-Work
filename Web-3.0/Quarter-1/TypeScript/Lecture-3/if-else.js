"use strict";
// If , Else and Else if
let marks = 78;
if (marks >= 90) {
    console.log("A+ Grade");
}
else if (marks >= 80) {
    console.log("A Grade");
}
else if (marks >= 70) {
    console.log("B Grade");
}
else if (marks >= 60) {
    console.log("C Grade");
}
else {
    console.log(" Fail ");
}
